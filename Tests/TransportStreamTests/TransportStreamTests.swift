        let partialPacket = try PartialTransportStreamPacket(bytes)
        
        XCTAssertEqual(partialPacket.transportErrorIndicator, false)
        XCTAssertEqual(partialPacket.payloadUnitStartIndicator, true)
        XCTAssertEqual(partialPacket.transportPriority, false)
        XCTAssertEqual(partialPacket.packetIdentifier, 0x0012)
        XCTAssertEqual(partialPacket.transportScramblingControl, 0)
        XCTAssertEqual(partialPacket.adaptationFieldControl.hasAdaptationField, false)
        XCTAssertEqual(partialPacket.adaptationFieldControl.hasPayload, true)
    }

    static var allTests = [
        ("testPTSPacket", testPTSPacket),
    ]
}
