        let bytes: [UInt8] = [
            0x47, 0x40, 0x12, 0x15, 0x00, 0x51, 0xf5, 0x2c, 0x44, 0x64, 0xe5, 0x48, 0x78, 0x04, 0x53, 0x00,
            0x01, 0x48, 0x51, 0x54, 0xf0, 0xe0, 0x25, 0x04, 0x17, 0x49, 0x00, 0x44, 0x55, 0x02, 0x3d, 0x4d,
