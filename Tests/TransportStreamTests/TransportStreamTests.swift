            0x27, 0x73, 0x20, 0x73, 0x63, 0x68, 0x72, 0xe4, 0x67, 0x20, 0x2d, 0x20, 0x44, 0x61, 0x73, 0x20,
            0x57, 0x65, 0x69, 0x68, 0x6e, 0x61, 0x63, 0x68, 0x74, 0x73, 0x73, 0x70, 0x65, 0x63, 0x69, 0x61,
            0x6c, 0x11, 0x05, 0x47, 0x61, 0x6d, 0x65, 0x73, 0x68, 0x6f, 0x77, 0x2c, 0x20, 0x44, 0x20, 0x32,
            0x30, 0x31, 0x34, 0x4e, 0xfe, 0x01, 0x44, 0x45, 0x55, 0x00, 0xf8, 0x05, 0x4d, 0x6f, 0x64, 0x65,
            0x72, 0x61, 0x74, 0x69, 0x6f, 0x6e, 0x3a, 0x20, 0x4a, 0x6f, 0x63, 0x68, 0x65, 0x6e, 0x20, 0x53,
            0x63, 0x68, 0x72, 0x6f, 0x70, 0x70, 0x0a, 0x0a, 0x4f, 0x68, 0x2c, 0x20, 0x6f, 0x68, 0x2c, 0x20,
            0x6f, 0x68, 0x20, 0x73, 0x74, 0x61, 0x74, 0x74, 0x20, 0x68, 0x6f, 0x2c, 0x20, 0x68, 0x6f, 0x2c,
            0x20, 0x68, 0x6f, 0x3a, 0x20, 0x53, 0x41, 0x54, 0x2e, 0x31, 0x20, 0x73, 0x63, 0x68, 0x69, 0x63,
            0x6b, 0x74, 0x20, 0x53, 0x63, 0x68, 0x61, 0x75, 0x73, 0x70, 0x69, 0x65
